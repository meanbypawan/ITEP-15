/*
  Relational Operator 
  >, <, >=, <=, ==, !=
  
  20 > 10
  20 < 10
  20 == 10  --------> true / false
  20 <= 10
  20 <= 10
  20 != 10
*/
class TestMain{
    public static void main(String args[]){
      System.out.println(" 20 > 20 : "+(20 > 20)); // false 
      System.out.println("20 >= 20 :"+(20 >= 20));
      System.out.println("20 == 20  :"+(20 == 20)); //true      
      System.out.println("20 != 20  :"+(20 != 20)); // false
    }
}







